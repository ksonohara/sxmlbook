/* ---------------------------------------------------------------------------------------------
 *  Function Section
 * --------------------------------------------------------------------------------------------- */
mikan.util.escape = function(v, html) {
	if (html == undefined) var html = false;
	try {
		if (html) {
			return v.replace(/</g, "&lt;").replace(/>/g, "&gt;");
		} else {
			return $('<span />').text(val).html();
		}
	} catch(e) {
		return "";
	}
};

